N=15;
y=[0 -0.1115 -0.2165 -0.3095 -0.3855 -0.4415 -0.4735 -0.4795 -0.4585 -0.4125 -0.345 -0.2635 -0.1745 -0.0905 -0.0265 0];
X=linspace(0,360,N+1);
h=(X(1)+X(2))/2;
D=[];
for i=1:N+1
    if i==1
        d=(y(i+3)-4*y(i+2)+5*y(i+1)-2*y(i))/h^2;
    elseif i==N+1
        d=(2*y(i)-5*y(i-1)+4*y(i-2)-y(i-3))/h^2;
    else
        d=(y(i+1)-2*y(i)+y(i-1))/h^2;
    end
    D(i)=d;
end
M=D*29*10^6*730;
plot(X,M,'-ro')
        