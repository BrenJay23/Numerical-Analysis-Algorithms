N=10;
T=[473.5 446.5 423.5 401.5 383.5 365.5 348.5 332.5 319.5 304.5 290.5];
X=linspace(0,10,N+1)/100;
D=[];
for i=1:N+1
    if i==1
        d=(-3*T(i)+4*T(i+1)-T(i+2))/(2*(1));
    elseif i==N+1
        d=(3*T(i)-4*T(i-1)+T(i-2))/(2*(1));
    else
        d=(T(i+1)-T(i-1))/(2*(1));
    end
    D(i)=d;
end
f=-240.5*(D);
plot(X,f,'-ro')
        